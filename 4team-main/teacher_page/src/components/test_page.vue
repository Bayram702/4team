<script>
export default {
  name: 'TestPage',
  data() {
    return {
      tests: [],
      loading: false,
      error: null,
      currentFilter: 'all',
      favorites: new Set() // –î–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∏–∑–±—Ä–∞–Ω–Ω—ã—Ö —Ç–µ—Å—Ç–æ–≤
    }
  },
  computed: {
    filteredTests() {
      if (this.currentFilter === 'all') {
        return this.tests;
      }
      
      // –ó–¥–µ—Å—å –º–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å –ª–æ–≥–∏–∫—É —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏–∏ –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º
      // –ü–æ–∫–∞ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –≤—Å–µ —Ç–µ—Å—Ç—ã, —Ç–∞–∫ –∫–∞–∫ —É –≤–∞—Å –≤ API –Ω–µ—Ç –∫–∞—Ç–µ–≥–æ—Ä–∏–π
      return this.tests;
    }
  },
  methods: {
    async fetchTests() {
      this.loading = true;
      this.error = null;
      
      try {
        const response = await fetch('/api/Test');
        
        if (!response.ok) {
          throw new Error(`–û—à–∏–±–∫–∞ HTTP: ${response.status}`);
        }
        
        const tests = await response.json();
        
        // –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –¥–∞–Ω–Ω—ã–µ –∏–∑ API –≤ —Ñ–æ—Ä–º–∞—Ç –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–∞
        this.tests = tests.map(test => ({
          id: test.id,
          title: test.title,
          description: test.description,
          timeLimitMinutes: test.timeLimitMinutes,
          passingScore: test.passingScore,
          difficulty: this.calculateDifficulty(test),
          questionCount: this.estimateQuestionCount(test),
          progress: this.getUserProgress(test.id), // –ó–¥–µ—Å—å –Ω—É–∂–Ω–æ –ø–æ–ª—É—á–∏—Ç—å —Ä–µ–∞–ª—å–Ω—ã–π –ø—Ä–æ–≥—Ä–µ—Å—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
          isFavorite: this.favorites.has(test.id)
        }));
        
      } catch (error) {
        console.error('–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ —Ç–µ—Å—Ç–æ–≤:', error);
        this.error = '–ù–µ —É–¥–∞–ª–æ—Å—å –∑–∞–≥—Ä—É–∑–∏—Ç—å —Ç–µ—Å—Ç—ã. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ —Å–µ—Ä–≤–µ—Ä—É.';
        
        // –î–ª—è –¥–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏–∏, –µ—Å–ª–∏ API –Ω–µ–¥–æ—Å—Ç—É–ø–Ω–æ
        if (this.tests.length === 0) {
          this.loadSampleData();
        }
      } finally {
        this.loading = false;
      }
    },
 calculateDifficulty(test) {
      // –õ–æ–≥–∏–∫–∞ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è —Å–ª–æ–∂–Ω–æ—Å—Ç–∏ –Ω–∞ –æ—Å–Ω–æ–≤–µ –¥–∞–Ω–Ω—ã—Ö —Ç–µ—Å—Ç–∞
      if (test.passingScore >= 80) return 'hard';
      if (test.passingScore >= 60) return 'medium';
      return 'easy';
    },

    estimateQuestionCount(test) {
      // –û—Ü–µ–Ω–∫–∞ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –≤–æ–ø—Ä–æ—Å–æ–≤ –Ω–∞ –æ—Å–Ω–æ–≤–µ –≤—Ä–µ–º–µ–Ω–∏
      return Math.floor(test.timeLimitMinutes / 2);
    },

    getUserProgress(testId) {
      // –ó–¥–µ—Å—å –¥–æ–ª–∂–Ω–∞ –±—ã—Ç—å –ª–æ–≥–∏–∫–∞ –ø–æ–ª—É—á–µ–Ω–∏—è –ø—Ä–æ–≥—Ä–µ—Å—Å–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
      // –í—Ä–µ–º–µ–Ω–Ω–∞—è —Ä–µ–∞–ª–∏–∑–∞—Ü–∏—è - —Å–ª—É—á–∞–π–Ω—ã–π –ø—Ä–æ–≥—Ä–µ—Å—Å –¥–ª—è –¥–µ–º–æ–Ω—Å—Ç—Ä–∞—Ü–∏–∏
      const progress = localStorage.getItem(`test_progress_${testId}`);
      return progress ? parseInt(progress) : 0;
    },

    setFilter(filter) {
      this.currentFilter = filter;
    },

    toggleFavorite(testId) {
      if (this.favorites.has(testId)) {
        this.favorites.delete(testId);
      } else {
        this.favorites.add(testId);
      }
      
      // –û–±–Ω–æ–≤–ª—è–µ–º —Å–æ—Å—Ç–æ—è–Ω–∏–µ –≤ —Ç–µ—Å—Ç–∞—Ö
      const test = this.tests.find(t => t.id === testId);
      if (test) {
        test.isFavorite = this.favorites.has(testId);
      }
    },

    startTest(testId) {
      console.log(`–ù–∞—á–∏–Ω–∞–µ–º —Ç–µ—Å—Ç —Å ID: ${testId}`);
      // –ó–¥–µ—Å—å –º–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å –ª–æ–≥–∏–∫—É –¥–ª—è –Ω–∞—á–∞–ª–∞ —Ç–µ—Å—Ç–∞
      // –ù–∞–ø—Ä–∏–º–µ—Ä, –ø–µ—Ä–µ—Ö–æ–¥ –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü—É –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–≥–æ —Ç–µ—Å—Ç–∞
      this.$router.push(`/test/${testId}`);
    },

    getDifficultyClass(difficulty) {
      switch (difficulty) {
        case 'easy': return 'difficulty-easy';
        case 'medium': return 'difficulty-medium';
        case 'hard': return 'difficulty-hard';
        default: return 'difficulty-medium';
      }
    },

    getDifficultyText(difficulty) {
      switch (difficulty) {
        case 'easy': return '–õ—ë–≥–∫–∏–π';
        case 'medium': return '–°—Ä–µ–¥–Ω–∏–π';
        case 'hard': return '–°–ª–æ–∂–Ω—ã–π';
        default: return '–°—Ä–µ–¥–Ω–∏–π';
      }
    },

    getQuestionCountText(count) {
      if (!count) return '–í–æ–ø—Ä–æ—Å—ã –Ω–µ —É–∫–∞–∑–∞–Ω—ã';
      const lastDigit = count % 10;
      const lastTwoDigits = count % 100;
      
      if (lastTwoDigits >= 11 && lastTwoDigits <= 19) {
        return `${count} –≤–æ–ø—Ä–æ—Å–æ–≤`;
      }
      
      if (lastDigit === 1) {
        return `${count} –≤–æ–ø—Ä–æ—Å`;
      } else if (lastDigit >= 2 && lastDigit <= 4) {
        return `${count} –≤–æ–ø—Ä–æ—Å–∞`;
      } else {
        return `${count} –≤–æ–ø—Ä–æ—Å–æ–≤`;
      }
    },

    getProgressWidth(progress) {
      return `${progress}%`;
    },

    getProgressText(progress) {
      if (progress === 0) return '–ù–µ –Ω–∞—á–∞—Ç';
      if (progress === 100) return '–ó–∞–≤–µ—Ä—à–µ–Ω–æ';
      return `${progress}% –∑–∞–≤–µ—Ä—à–µ–Ω–æ`;
    },

    getButtonText(progress) {
      if (progress === 0) return '–ù–∞—á–∞—Ç—å —Ç–µ—Å—Ç';
      if (progress === 100) return '–ü–æ–≤—Ç–æ—Ä–∏—Ç—å';
      return '–ü—Ä–æ–¥–æ–ª–∂–∏—Ç—å';
    }
  },
  mounted() {
    this.fetchTests();
    console.log('–°—Ç—Ä–∞–Ω–∏—Ü–∞ —Ç–µ—Å—Ç–æ–≤ –∑–∞–≥—Ä—É–∂–µ–Ω–∞');
  }
}
